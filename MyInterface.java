
public interface MyInterface {
/*
 * In Java, interfaces are quite significant programs. They look like classes
 * but slightly different, in terms of their use and their structure.
 * Firstly they are not extended, instead they are implemented. Interfaces 
 * usually contain a few abstract functions with no contents. The class that 
 * implements an interface must have the function and fills up the content
 * 
 */
	public abstract void print(); 
}
